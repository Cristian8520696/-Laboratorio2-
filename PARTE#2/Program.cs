
// Operaciones con enteros
Console.WriteLine("Ingresa primer número entero:");
int entero1 = int.Parse(Console.ReadLine());

Console.WriteLine("Ingresa el segundo número entero:");
int entero2 = int.Parse(Console.ReadLine());

Console.WriteLine("La suma de " + entero1 + " + " + entero2 + " es = " + (entero1 + entero2));

Console.WriteLine("La resta de " + entero1 + " - " + entero2 + " es = " + (entero1 - entero2));

Console.WriteLine("La multiplicación de " + entero1 + " * " + entero2 + " es = " + (entero1 * entero2));

Console.WriteLine("La división de " + entero1 + " / " + entero2 + " es = " + (entero1 / entero2));

